PIECE_VALUES = {1: 100, 2: 500, 3: 330, 4: 320, 5: 900, 6: 20000}

def evaluate(board):
    score = 0
    for i in range(8):
        for j in range(8):
            piece = board[i][j]
            if piece == 0:
                continue
            abs_piece = abs(piece)
            table = TABLES[abs_piece]
            if piece > 0:  # white
                score += PIECE_VALUES[abs_piece]
                score += table[i * 8 + j]
            else:  # black
                score -= PIECE_VALUES[abs_piece]
                score -= table[(7 - i) * 8 + j]
    return score